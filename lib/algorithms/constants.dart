/// 判断三爻组合对应的八卦编号
/// 阳爻=1, 阴爻=0, 从下往上排列
/// code: 000=坤(8), 001=震(4), 010=坎(6), 011=兑(2)
///       100=艮(7), 101=离(3), 110=巽(5), 111=乾(1)
int getBaGuaId(bool y1, bool y2, bool y3) {
  int code = (y1 ? 1 : 0) + ((y2 ? 1 : 0) << 1) + ((y3 ? 1 : 0) << 2);
  const Map<int, int> codeToId = {
    0: 8, // 坤
    1: 4, // 震
    2: 6, // 坎
    3: 2, // 兑
    4: 7, // 艮
    5: 3, // 离
    6: 5, // 巽
    7: 1, // 乾
  };
  return codeToId[code]!;
}

